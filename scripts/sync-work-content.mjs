#!/usr/bin/env node
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const siteRoot = path.resolve(__dirname, '..');

const workItemsPath = path.join(siteRoot, 'resources', 'work-items.json');
const indexHtmlPath = path.join(siteRoot, 'index.html');
const workMdPath = path.join(siteRoot, 'work.md');

const WORK_START = '<!-- WORK_CAROUSEL_START -->';
const WORK_END = '<!-- WORK_CAROUSEL_END -->';
const REQUIRED_FIELDS = ['id', 'project', 'client', 'services', 'stack', 'image'];

function escapeHtml(value) {
  return String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;');
}

function renderSlides(items) {
  return items
    .map((item) => {
      const project = escapeHtml(item.project);
      const client = escapeHtml(item.client);
      const services = escapeHtml(item.services);
      const stack = escapeHtml(item.stack);
      const image = escapeHtml(item.image);

      return [
        '              <div class="embla__slide">',
        `                <article class="work-card" aria-label="${project} for ${client}">`,
        '                  <div class="work-picture">',
        `                    <img src="${image}" alt="${project}" width="340" height="190" />`,
        '                  </div>',
        '                  <div class="work-copy">',
        `                    <h3 class="work-project">${project}</h3>`,
        `                    <p class="work-client">${client}</p>`,
        '                    <div class="work-details">',
        `                      <p class="work-services">${services}</p>`,
        `                      <p class="work-stack">${stack}</p>`,
        '                    </div>',
        '                  </div>',
        '                </article>',
        '              </div>'
      ].join('\n');
    })
    .join('\n');
}

function normalizeStackForMarkdown(stack) {
  return String(stack).replace(/^Tech stack:\s*/i, '');
}

async function validateItems(items) {
  for (const [index, item] of items.entries()) {
    for (const field of REQUIRED_FIELDS) {
      if (typeof item[field] !== 'string' || !item[field].trim()) {
        throw new Error(`work-items.json item ${index} is missing a valid ${field}`);
      }
    }

    const imagePath = path.join(siteRoot, item.image);
    try {
      await fs.access(imagePath);
    } catch {
      throw new Error(`work-items.json item ${index} references a missing image: ${item.image}`);
    }
  }
}

function renderWorkMarkdown(items) {
  const lines = [
    '<!-- Generated by scripts/sync-work-content.mjs from resources/work-items.json. Do not edit manually. -->',
    '# Selected work from Keryx Solutions',
    '',
    '> The portfolio entries below mirror the current public work carousel data.',
    '',
    '## Portfolio entries',
    ''
  ];

  for (const item of items) {
    lines.push(`### ${item.project} — ${item.client}`);
    lines.push('');
    lines.push(`- **Services:** ${item.services}`);
    lines.push(`- **Stack:** ${normalizeStackForMarkdown(item.stack)}`);
    lines.push(`- **Image:** \`${item.image}\``);
    lines.push('');
  }

  return `${lines.join('\n')}\n`;
}

async function main() {
  const [itemsRaw, indexHtmlRaw] = await Promise.all([
    fs.readFile(workItemsPath, 'utf8'),
    fs.readFile(indexHtmlPath, 'utf8')
  ]);

  const items = JSON.parse(itemsRaw);
  if (!Array.isArray(items) || !items.length) {
    throw new Error('resources/work-items.json must contain a non-empty array');
  }

  await validateItems(items);

  const slides = renderSlides(items);
  const generatedRegion = `${WORK_START}\n${slides}\n              ${WORK_END}`;

  if (!indexHtmlRaw.includes(WORK_START) || !indexHtmlRaw.includes(WORK_END)) {
    throw new Error('index.html is missing WORK_CAROUSEL markers');
  }

  const indexHtmlNext = indexHtmlRaw.replace(
    /<!-- WORK_CAROUSEL_START -->[\s\S]*?<!-- WORK_CAROUSEL_END -->/,
    generatedRegion
  );

  const workMdNext = renderWorkMarkdown(items);

  await fs.writeFile(indexHtmlPath, indexHtmlNext);
  await fs.writeFile(workMdPath, workMdNext);

  console.log(`Synced ${items.length} work items to index.html and work.md`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
