#!/usr/bin/env node
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const siteRoot = path.resolve(__dirname, '..');

const workItemsPath = path.join(siteRoot, 'resources', 'work-items.json');
const indexHtmlPath = path.join(siteRoot, 'index.html');
const workMdPath = path.join(siteRoot, 'work.md');

const WORK_START = '<!-- WORK_CAROUSEL_START -->';
const WORK_END = '<!-- WORK_CAROUSEL_END -->';
const FEATURED_START = '<!-- FEATURED_WORK_START -->';
const FEATURED_END = '<!-- FEATURED_WORK_END -->';
const REQUIRED_FIELDS = ['id', 'project', 'client', 'services', 'stack', 'image'];

function escapeHtml(value) {
  return String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;');
}

function renderSlides(items) {
  return items
    .map((item) => {
      const project = escapeHtml(item.project);
      const client = escapeHtml(item.client);
      const services = escapeHtml(item.services);
      const stack = escapeHtml(item.stack);
      const image = escapeHtml(item.image);

      return [
        '              <div class="embla__slide">',
        `                <article class="work-card" aria-label="${project} for ${client}">`,
        '                  <div class="work-picture">',
        `                    <img src="${image}" alt="${project}" width="340" height="190" />`,
        '                  </div>',
        '                  <div class="work-copy">',
        `                    <h3 class="work-project">${project}</h3>`,
        `                    <p class="work-client">${client}</p>`,
        '                    <div class="work-details">',
        `                      <p class="work-services">${services}</p>`,
        `                      <p class="work-stack">${stack}</p>`,
        '                    </div>',
        '                  </div>',
        '                </article>',
        '              </div>'
      ].join('\n');
    })
    .join('\n');
}

function normalizeStackHtml(stack) {
  return String(stack).replace(/^Tech stack:\s*/i, '');
}

async function imageExists(relativePath) {
  try {
    await fs.access(path.join(siteRoot, relativePath));
    return true;
  } catch {
    return false;
  }
}

async function renderFeaturedCards(items) {
  const featured = items
    .filter((item) => item.type === 'current')
    .slice(0, 3);

  if (!featured.length) {
    throw new Error('work-items.json must contain at least one item with "type": "current" for featured cards');
  }

  const cards = [];
  for (const item of featured) {
    const project = escapeHtml(item.project);
    const client = escapeHtml(item.client);
    const services = escapeHtml(item.services);
    const stack = escapeHtml(normalizeStackHtml(item.stack));

    cards.push([
      '            <article class="featured-card">',
      `              <div class="ipad-device ipad-device--portrait ipad-device--half" aria-hidden="true">`,
      `                <img class="ipad-screenshot" src="${escapeHtml(item.image)}" alt="" loading="lazy" />`,
      '              </div>',
      '              <div class="featured-copy">',
      '                <span class="featured-badge">Recent Work</span>',
      `                <h3 class="featured-project">${project}</h3>`,
      `                <p class="featured-client">${client}</p>`,
      `                <p class="featured-services">${services}</p>`,
      `                <p class="featured-stack">${stack}</p>`,
      '              </div>',
      '            </article>'
    ].join('\n'));
  }

  return cards.join('\n');
}

function normalizeStackForMarkdown(stack) {
  return String(stack).replace(/^Tech stack:\s*/i, '');
}

async function validateItems(items) {
  for (const [index, item] of items.entries()) {
    for (const field of REQUIRED_FIELDS) {
      if (typeof item[field] !== 'string' || !item[field].trim()) {
        throw new Error(`work-items.json item ${index} is missing a valid ${field}`);
      }
    }

    const imagePath = path.join(siteRoot, item.image);
    try {
      await fs.access(imagePath);
    } catch {
      console.warn(`Warning: work-items.json item ${index} references a missing image: ${item.image}`);
    }
  }
}

function renderWorkMarkdown(items) {
  const featured = items.filter((i) => i.type === 'current');
  const career = items.filter((i) => i.type !== 'current');

  const lines = [
    '<!-- Generated by scripts/sync-work-content.mjs from resources/work-items.json. Do not edit manually. -->',
    '# Selected work from Keryx Solutions',
    '',
    '> The portfolio entries below mirror the current public work data.',
    ''
  ];

  if (featured.length) {
    lines.push('## Recent Keryx Projects');
    lines.push('');
    for (const item of featured) {
      lines.push(`### ${item.project} — ${item.client}`);
      lines.push('');
      lines.push(`- **Services:** ${item.services}`);
      lines.push(`- **Stack:** ${normalizeStackForMarkdown(item.stack)}`);
      lines.push(`- **Image:** \`${item.image}\``);
      lines.push('');
    }
  }

  lines.push('## Career highlights');
  lines.push('');

  for (const item of career) {
    lines.push(`### ${item.project} — ${item.client}`);
    lines.push('');
    lines.push(`- **Services:** ${item.services}`);
    lines.push(`- **Stack:** ${normalizeStackForMarkdown(item.stack)}`);
    lines.push(`- **Image:** \`${item.image}\``);
    lines.push('');
  }

  return `${lines.join('\n')}\n`;
}

async function main() {
  const [itemsRaw, indexHtmlRaw] = await Promise.all([
    fs.readFile(workItemsPath, 'utf8'),
    fs.readFile(indexHtmlPath, 'utf8')
  ]);

  const items = JSON.parse(itemsRaw);
  if (!Array.isArray(items) || !items.length) {
    throw new Error('resources/work-items.json must contain a non-empty array');
  }

  await validateItems(items);

  const carouselItems = items.filter((item) => item.type !== 'current');
  const slides = renderSlides(carouselItems);
  const generatedRegion = `${WORK_START}\n${slides}\n              ${WORK_END}`;

  if (!indexHtmlRaw.includes(WORK_START) || !indexHtmlRaw.includes(WORK_END)) {
    throw new Error('index.html is missing WORK_CAROUSEL markers');
  }

  let indexHtmlNext = indexHtmlRaw.replace(
    /<!-- WORK_CAROUSEL_START -->[\s\S]*?<!-- WORK_CAROUSEL_END -->/,
    generatedRegion
  );

  if (indexHtmlNext.includes(FEATURED_START) && indexHtmlNext.includes(FEATURED_END)) {
    const featuredCards = await renderFeaturedCards(items);
    const featuredRegion = `${FEATURED_START}\n${featuredCards}\n            ${FEATURED_END}`;
    indexHtmlNext = indexHtmlNext.replace(
      /<!-- FEATURED_WORK_START -->[\s\S]*?<!-- FEATURED_WORK_END -->/,
      featuredRegion
    );
  }

  const workMdNext = renderWorkMarkdown(items);

  await fs.writeFile(indexHtmlPath, indexHtmlNext);
  await fs.writeFile(workMdPath, workMdNext);

  const currentCount = items.filter((i) => i.type === 'current').length;
  console.log(`Synced ${carouselItems.length} carousel + ${currentCount} featured items to index.html and work.md`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
